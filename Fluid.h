#pragma once
#include <Arduino.h>

// =============================================================
// Fluid
//   待機画面用の流体シミュレーション。
//   基板の QMI8658 IMU（I2C SDA=11 / SCL=12）から傾きを読み、
//   擬似重力で粒（水滴）を動かして 8x8 マトリクスに描画する。
//
//   粒の数  = 受信データの件数（Inbox::size()）
//   粒の色  = 各受信データの代表色（最も鮮やかなピクセル色）
//
//   描画は Display::Internal::matrix() を通して行う
//   （FastLED ではなく既存の Adafruit_NeoMatrix を使用）。
// =============================================================
namespace Fluid {

    // IMU 初期化＋粒の初期化。setup() の設定ロード後に呼ぶ。
    void init();

    // Inbox の内容から粒の数と色を作り直す。
    //   起動時・受信時・リセット時に呼ぶ。
    void syncFromInbox();

    // 1 フレーム進めて描画する。待機中に loop() から毎回呼ぶ。
    void tick();

}
